/**
 * Transform curriculum data for database insertion
 */
export function transformCurriculumForDB(items: CurriculumItem[]) {
  const grouped = new Map<string, any>();

  items.forEach(item => {
    const key = `${item.class}_${item.strand}_${item.sub_strand}_${item.content_standard_code}`;
    
    if (!grouped.has(key)) {
      grouped.set(key, {
        curriculum_name: `Ghana Computing Curriculum ${item.class}`,
        grade_level: item.class,
        subject: "Computing",
        strand: item.strand,
        sub_strand: item.sub_strand,
        content_standard_code: item.content_standard_code,
        content_standards: [item.content_standard_description],
        learning_indicators: [item.indicator_description],
        is_public: true,
      });
    } else {
      const existing = grouped.get(key);
      if (!existing.learning_indicators.includes(item.indicator_description)) {
        existing.learning_indicators.push(item.indicator_description);
      }
    }
  });

  return Array.from(grouped.values());
}
